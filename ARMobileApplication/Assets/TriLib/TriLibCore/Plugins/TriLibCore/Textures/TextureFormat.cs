namespace TriLibCore.General
{
    /// <summary>
    /// Specifies the format used to represent texture data.
    /// </summary>
    public enum TextureFormat
    {
        /// <summary>
        /// Represents a texture format with red, green, blue, and alpha channels.
        /// </summary>
        RGBA,

        /// <summary>
        /// Represents a normalized unsigned format, typically used for single-channel
        /// or non-color texture data.
        /// </summary>
        UNorm
    }
}
